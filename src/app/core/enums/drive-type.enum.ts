export enum DriveType {
  SHAFT = 'shaft',            // Inboard shaft drive
  SAILDRIVE = 'saildrive',    // Common on sailboats
  POD = 'pod',                // Volvo IPS, Zeus
  OUTBOARD = 'outboard',      // External engine
  STERNDRIVE = 'sterndrive',  // I/O (MerCruiser)
  JET = 'jet',                // Jet propulsion
  SURFACE = 'surface',        // Surface drive (Arneson)
}